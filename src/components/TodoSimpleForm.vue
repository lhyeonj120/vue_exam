<template>
    <form 
        @submit.prevent="onSubmit"  
    >
        <div class="d-flex">
        <div class="flex-grow-1 mr-2">
            <input 
            class="form-control"
            type="text" 
            v-model="todo"
            placeholder="Type new to-do"
            > 
        </div>

        <div>
            <button 
            class="btn btn-primary"
            type="submit"
            >
            Add
            </button>
        </div>
        </div>
        <div v-if="hasError" style="color:red">This is cannot be empty</div>
    </form>
</template>

<script>
import {ref} from 'vue';
export default {
    setup(){
        const todo = ref('');
        const hasError = ref(false);

        const onSubmit = () => {
            if(todo.value == ''){
                hasError.value = true;
            }else{
                // todos.value.push({
                //     id: Date.now(),
                //     subject: todo.value,
                //     completed: false
                // }); //객체 형태로 넣기
                hasError.value = false;
                todo.value = ''
            }
        }

        return{
            todo,
            hasError,
            onSubmit,
        }
    }
}
</script>

